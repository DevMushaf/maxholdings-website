function handleSubscribe(event, form) {
    event.preventDefault();
    
    const emailInput = form.querySelector('.newsletter-email');
    const button = form.querySelector('.newsletter-btn');
    const btnText = form.querySelector('.btn-text');
    const btnIcon = form.querySelector('.btn-icon');
    const errorMessage = form.querySelector('.error-message');
    const email = emailInput.value.trim();
    
    // Email validation regex
    const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
    
    // Reset error message
    errorMessage.classList.add('hidden');
    errorMessage.textContent = '';
    
    // Validate email format
    if (!emailRegex.test(email)) {
        errorMessage.textContent = 'Please enter a valid email address';
        errorMessage.classList.remove('hidden');
        emailInput.classList.add('border-red-500');
        
        // Remove error styling after 3 seconds
        setTimeout(() => {
            errorMessage.classList.add('hidden');
            emailInput.classList.remove('border-red-500');
        }, 3000);
        return;
    }
    
    // Disable button and show loading state
    button.disabled = true;
    btnText.textContent = 'Subscribing...';
    btnIcon.className = 'fas fa-spinner fa-spin';
    
    // Simulate API call (replace with actual backend call)
    setTimeout(() => {
        // Success state
        button.style.background = 'linear-gradient(135deg, #10b981 0%, #059669 100%)';
        btnText.textContent = 'Subscribed!';
        btnIcon.className = 'fas fa-check';
        
        // Disable input
        emailInput.disabled = true;
        emailInput.classList.add('opacity-50');
        
        setTimeout(() => {
            button.disabled = false;
            button.style.background = 'linear-gradient(135deg, #FF2212 0%, #FF4500 100%)';
            btnText.textContent = 'Subscribe Now';
            btnIcon.className = 'fas fa-paper-plane';
            emailInput.disabled = false;
            emailInput.classList.remove('opacity-50');
            emailInput.value = '';
        }, 1000);
        
        
    }, 1000);
}
